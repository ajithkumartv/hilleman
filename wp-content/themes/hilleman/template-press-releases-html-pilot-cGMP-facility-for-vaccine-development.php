                    <p class="hl-media-release__txt">Singapore, 22 February 2022: Hilleman Laboratories, an organisation with a mission to develop 
affordable vaccines and biologics against infectious diseases, today broke ground on a 30,000 square 
foot current Good Manufacturing Practices (cGMP) facility at 138 Depot Road as part of its official 
launch in Singapore. The ceremony was graced by Mr Gan Kim Yong, Minister for Trade and Industry, 
Singapore and was also attended by members of the Hilleman Laboratories Board of Directors, namely 
Chairman Mr Sanat Chattopadhyay, Dr Gerd Zettlmeissl, Professor Gagandeep Kang, and CEO Dr 
Raman Rao, as well as representatives of Hilleman Laboratories’ partner organisations.
                    </p>
                    
                    <p class="hl-media-release__txt">The cGMP facility, together with an existing research and development (R&D) facility at Biopolis, form
an SGD 80 million (USD 58 million) vaccine and biologics development and manufacturing hub, the first 
of its kind in Singapore. The hub will provide end-to-end product development solutions from concept 
to cGMP and Phase II clinical development, supporting the development of affordable, high-value 
vaccines and biologics for Singapore as well as low- and middle-income countries using innovative 
technology platforms. Expected to be fully operational by early 2023, the cGMP facility will supply 
clinical trial materials up to Phase II stage, and has been designed to pivot to manufacture vaccines for 
Singapore’s use during pandemics.</p>

                    <p class="hl-media-release__txt">“This is a major milestone for Hilleman Laboratories as we embark on our next phase of growth. We 
look forward to building strong partnerships with stakeholders across the vaccine development 
landscape with Singapore as our headquarters. We are also committed to supporting the enhancement 
of local capabilities in vaccines and biologics development to shore up Singapore’s defences against 
disease outbreaks, particularly those with pandemic potential,” said Dr Rao.</p>

                    <p class="hl-media-release__txt">“Hilleman Laboratories’ new facility is a welcome addition to Singapore’s healthcare ecosystem, and is 
in line with our efforts to strengthen capabilities across the biopharmaceutical manufacturing value 
chain. With its focus on early clinical development of vaccines and biologics for diseases that exert a 
heavy burden globally, the new facility will enhance Singapore’s status as a global hub for 
biopharmaceutical manufacturing, and stand us in good stead to meet national needs during future 
pandemics,” said Ms Goh Wan Yee, Senior Vice President, Healthcare, Singapore Economic 
Development Board.</p>

                    <p class="hl-media-release__txt">Hilleman Laboratories plans to hire up to 50 scientists, engineers, and managers locally, in addition to 
its current staff. It will offer training and career development opportunities to strengthen domestic
expertise in the development of vaccines and biologics in compliance with global regulatory and quality 
standards.
