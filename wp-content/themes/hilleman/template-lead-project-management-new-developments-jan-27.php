            <h3 class="hl-job-description__job-hd"> Responsibilities:</h3>
            <ul>
<li>Proactively manage and oversee key elements of projects through monitoring, in-charge of the scope, timelines, risk, budget and quality, striving for an optimum use of resources, cost versus timelines and risk.
</li>
<li>Define specific Product Development strategy, including for early development programs, and help the team with early R&D plan roll out definition.
</li>
<li>Assess and provide feedback to cross-functional technical proposals and recommendations.
</li>
<li>Manage multiple workstreams and projects. Make sound and timely decisions based on project priorities. Discern paths forward within ambiguous environments.
</li>
<li>Maintain a deep and broad understanding of vaccine and biologics development and manufacturing, the regulatory environment, life cycle management, trends in innovation, and financial and resource impact.
</li>
<li>Develop a clear understanding of the content of respective HLS projects and maintain relevant knowledge throughout the project life cycle.
</li>
<li>Support due diligence and expert review of proposals; develop good understanding of the project subject.
</li>
<li>Establish a strong working relationship with respective project team members and functional leads to jointly ensure timely deliverables.
</li>
<li>Guide team members in the execution of operational plans. 
</li>
<li>Track project progress against timelines, financial and quality milestones as well as report to management and, where asked, to investors. Proactively identify issues and / or obstacles, working with subject matter experts to develop resolutions and contingencies to guide the project to a successful outcome. 
</li>
<li>Closely manage project risks to ensure that mitigating activities are appropriately considered. Ensure that risks are sufficiently and regularly briefed to management
</li>
<li>Identify synergies and efficiencies, facilitate collaboration and communication between different projects; identifying ways to accelerate project execution where possible while maintaining high quality standards.
</li>
<li>Manage project team related documentation and communications. Assist with determination of cross-functional communication and coordination of action items. 
</li>
<li>Actively contribute to portfolio review and analysis, working closely with colleagues across the organization, external experts and Scientific Advisory Committee, contributing to continuous improvement.
</li>
<li>Being a coach and mentor for team members that may require more guidance.
</li>
<li>Provides vision and passion for driving technology and innovation within the vaccines and biologics portfolio
</li>
<li>Able to operate effectively through ambiguity.
</li>
<li>Ability to influence broadly across the organization to drive decisions, alignment, and conflict resolution for complex cross-functional issues
</li>
<li>Is able to effectively manage multiple products
</li>
<li>Displays and drives Hilleman’s corporate values of innovation, respect and passion, trust, integrity and agility, safety, and quality into the organization
</li>
<li>Draws in others focused on innovation to drive better outcomes from the product.
</li>
<li>Provide scientific leadership to a team of internal experts working on the various programs, be a mentor and resource to junior team members
</li>
            </ul>
